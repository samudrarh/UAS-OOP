public class MatematikaCanggihBeraksi {
     public static void main(String[] args) {
        // Membuat objek dari kelas MatematikaCanggihBangetBeraksi
        MatematikaCanggihBangetBeraksi mtk = new MatematikaCanggihBangetBeraksi();
        
        mtk.Pertambahan(5, 3);         // Hasil: 5 + 3 = 8
        mtk.Perkalian(5, 3);           // Hasil: 5 * 3 = 15
        mtk.Modulus(5, 3);             // Hasil: 5 % 3 = 2
        mtk.PertambahanTiga(5, 3, 4);  // Hasil: 5 + 3 + 4 = 12
    }
}

